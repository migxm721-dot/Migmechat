	<div id="faq7" style="display: none">
		<div class="hd">What if my account is not credited?</div>
		<div class="bd">
<blockquote><p>Don't worry!  sometimes this can happen if there was incorrect information filled out on the Western Union form.  We recommend that you keep your receipt as proof of payment.  We will use this to trace your payment.  Please feel free to email us your receipt number, name and amount to <a href="mailto:merchant@mig33.com" target="_blank">merchant@mig33.com</a> if your account is not credited after 24 hours.</p>
<p>Please note that completing a Western Union payment request online is not enough to get credits added to your migme username.  You must complete a payment using the GREEN QuickCash form with a Western Union agent before we can add credits.</p>
</p></blockquote>

		</div>
	</div>

