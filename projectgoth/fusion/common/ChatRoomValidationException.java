package com.projectgoth.fusion.common;

public class ChatRoomValidationException extends Exception {
   public ChatRoomValidationException() {
   }

   public ChatRoomValidationException(String arg0) {
      super(arg0);
   }

   public ChatRoomValidationException(Throwable arg0) {
      super(arg0);
   }

   public ChatRoomValidationException(String arg0, Throwable arg1) {
      super(arg0, arg1);
   }
}
